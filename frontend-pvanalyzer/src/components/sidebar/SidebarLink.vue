<template>
  <div v-if="to">
    <router-link :to="to" class="link" :class="{ active: isActive }">
      <i class="icon" :class="icon" />
      <span v-if="!collapsed">
        <slot />
      </span>
    </router-link>
  </div>
  <div v-else class="link">
    <i class="icon" :class="icon" />
    <span v-if="!collapsed">
      <slot />
    </span>
  </div>
</template>

<script>
import { computed } from "vue";
import { useRoute } from "vue-router";

export default {
  props: {
    to: {
      type: String,
      required: false,
    },
    icon: {
      type: String,
      required: true,
    },
    collapsed:{
      type: Boolean,
    }
  },
  setup(props) {
    const route = useRoute();
    const isActive = computed(() => route.path === props.to);
    return { isActive, };
  },
};
</script>

